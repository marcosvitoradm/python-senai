# Peça ao usuário para inserir dois números
num1 = float(input("Digite o primeiro número. \n"))
num2 = float(input('Digite o segundo número. \n'))

# Verifique se ambos são múltiplos de 5.
if num1 % 5 == 0:
    print('Este número é multiplo de 5.')
if num2 % 5 == 0:
    print('Este número é multiplo de 5.')
else:
    print('Este não é um número multiplo de 5. Tente outro, por favor')





