# Peça ao usuário para inserir um número.
numero = int(input('Insira um número: \n'))

# Exiba os números de 1 até esse número, mas de forma decrescente.

def mais1(numero):
    contagem = -1
    numero + contagem
    contagem += 1
    return contagem
    
for i in range(numero):
    print(mais1)

    
