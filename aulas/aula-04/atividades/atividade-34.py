# Peça ao usuário um número. 
num1 = float(input('Digite aqui um número: \n'))

# Verifique se ele é positivo, negativo ou zero.
if num1 > 0:
    print(f'Seu número {num1} é positivo.')
elif num1 < 0:
    print(f'Seu número é {num1} é negativo.')
elif num1 == 0:
    print(f'Seu número é 0.')
    





